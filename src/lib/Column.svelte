  <h2>Columns</h2>
  <p>The layout grid of the app is based on 
    <cyan-code>.Column</cyan-code> elements positoined by the main layout flex-box('s).</p>
  <p>A Column will be automatically fit to a screen size - matching it's desired layout classes, where possible.</p>
  <h3>Sizing classes</h3>
  <p>Columns can be sized by adding one of the following classes:</p>
  <ul>
    <li><cyan-code>.full</cyan-code> - full width</li>
    <li><cyan-code>.large</cyan-code> - for content that should not be wider than the maximum recommended
      line-lenght for the given viewport and screen size.</li>
    <li><cyan-code>[.medium]</cyan-code> - Default width, does not need to be set with a class.</li>
    <li><cyan-code>.small</cyan-code> - for content that can be shown in a narrow element, but still should
      be wide enough to be usable within the viewport and screen size.</li>
