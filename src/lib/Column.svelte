  <h2>Columns</h2>
  <p>The layout grid of the app is based on 
    <cyan-code>.Column</cyan-code> elements positoined by the main layout flex-box.</p>
  <p>A Column will be automatically fit to a screen size - matching it's desired layout classes, where possible.</p>
  <h3>Sizing classes</h3>
  <p>Columns can be sized by adding one of the following classes:</p>
  <ul>
    <li><cyan-code>.full</cyan-code> - full width</li>
    <li><cyan-code>.half</cyan-code> - half width</li>
    <li><cyan-code>.third</cyan-code> - third width</li>
    <li><cyan-code>.quarter</cyan-code> - quarter width</li>
</article>